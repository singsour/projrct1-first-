//몹 싸움전 나오는 간단한 아트
void spawn_mob_event(){

    int left = 0, right = COLUMNS - 1,up=0, down=ROWS-1;
    char screen[ROWS][COLUMNS + 1];
    srand(time(NULL));
    int event= rand()%2+1;
    
    if(event==1){        
        // 화면 초기화
        for (int i = 0; i < ROWS; i++) {
            for (int j = 0; j < COLUMNS; j++) {
                screen[i][j] = ' ';
            }
            screen[i][COLUMNS] = '\0'; // 문자열 끝 표시
        }

        while (left <= right) {
            // 화면을 지웁니다
            system("cls");

            // 양쪽에서 `0`을 채워 넣습니다.
            for (int i = 0; i < ROWS; i++) {
                screen[i][left] = '0';
                screen[i][right] = '0';
            }

            // 화면을 출력합니다
            for (int i = 0; i < ROWS; i++) {
                printf("%s\n", screen[i]);
            }

            // 양쪽에서 한 칸씩 중앙으로 이동
            left++;
            right--;

            Sleep(250); // 애니메이션 속도 조절 (100 밀리초 = 0.1초)
            
        } 
        system("cls");
    }else{
        // 화면 초기화
        for (int i = 0; i < ROWS; i++) {
            for (int j = 0; j < COLUMNS; j++) {
                screen[i][j] = ' ';
            }
            screen[i][COLUMNS] = '\0'; // 문자열 끝 표시
        }
        while (up <= down) {
            // 화면을 지웁니다
            system("cls");

            // 위 아래에서 `0`을 채워 넣습니다.
            for (int i = 0; i < COLUMNS; i++) {
                screen[up][i] = '0';
                screen[down][i] = '0';
            }

            // 화면을 출력합니다
            for (int i = 0; i < ROWS; i++) {
                printf("%s\n", screen[i]);
            }

            // 양쪽에서 한 칸씩 중앙으로 이동
            up++;
            down--;

            Sleep(250); // 애니메이션 속도 조절 (100 밀리초 = 0.1초)
        } 
        system("cls");
        
    }
    
}
